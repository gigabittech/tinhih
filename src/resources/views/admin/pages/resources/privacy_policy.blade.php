              <h3>Naloxone</h3>
              
              <p>Naloxone is a medicine that rapidly reverses an opioid overdose. It is an opioid antagonist. This means that it attaches to opioid receptors and reverses and blocks the effects of other opioids. Naloxone can quickly restore normal breathing to a person if their breathing has slowed or stopped because of an opioid overdose.</p>
              
